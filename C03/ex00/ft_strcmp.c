int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	c;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		c = s1[i] - s2[i];
		if (c != 0)
		{
			return (c);
		}	
		i++;
	}
	return (0);
}
